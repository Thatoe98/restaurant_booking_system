// Language Manager Class
export class LanguageManager {
  constructor() {
    this.currentLang = localStorage.getItem('language') || 'en'
  }
  
  t(key) {
    return translations[this.currentLang]?.[key] || translations.en[key] || key
  }
  
  setLanguage(lang) {
    this.currentLang = lang
    localStorage.setItem('language', lang)
    this.applyTranslations()
  }
  
  applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(element => {
      const key = element.getAttribute('data-i18n')
      const translation = this.t(key)
      
      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        if (element.type === 'submit' || element.type === 'button') {
          element.value = translation
        } else {
          element.placeholder = translation
        }
      } else if (element.tagName === 'BUTTON') {
        // Preserve icons if they exist
        const icon = element.querySelector('.lang-icon, .btn-icon, .step-icon, .action-icon')
        if (icon) {
          const iconHTML = icon.outerHTML
          element.innerHTML = iconHTML + ' ' + translation
        } else {
          element.textContent = translation
        }
      } else {
        element.textContent = translation
      }
    })
    
    // Update HTML lang attribute
    document.documentElement.lang = this.currentLang === 'my' ? 'my' : 'en'
    
    // Trigger custom event for dynamic content updates
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang: this.currentLang } }))
  }
  
  getCurrentLanguage() {
    return this.currentLang
  }
}
